MERGE INTO USUARIO U
USING (
    SELECT
    1 AS ID_USUARIO_S,
    'KENNET' AS NOMBRE_USUARIO_S,
    'Kennet' AS PRIMER_NOMBRE_S,
    'Jorshoa' AS SEGUNDO_NOMBRE_S,
    'Ortega' AS PRIMER_APELLIDO_S,
    'Quiroz' AS SEGUNDO_APELLIDO_S,
    'N/A' AS OTROS_APELLIDOS_S,
    '1234' AS PASSWORD_S,
    'N/A' AS CALLE_S,
    'N/A' AS COLONIA_S,
    '0' AS ZONA_S,
    'Guatemala' AS CIUDAD_S,
    3 AS MUNICIPIO_S,
    1 AS DEPARTAMENTO_S,
    '1010' AS CODIGO_POSTAL_S,
    '58764583' AS TELEFONO_S,
    '109162544' AS NIT_S,
    '3672190180101' AS DPI_S,
    1 AS ID_ROL_S
    FROM DUAL
    ) S
ON (U.ID_USUARIO = S.ID_USUARIO_S)
WHEN MATCHED THEN
UPDATE SET
    U.NOMBRE_USUARIO = S.NOMBRE_USUARIO_S,
    U.PRIMER_NOMBRE = S.PRIMER_NOMBRE_S,
    U.SEGUNDO_NOMBRE = S.SEGUNDO_NOMBRE_S,
    U.PRIMER_APELLIDO = S.PRIMER_APELLIDO_S,
    U.SEGUNDO_APELLIDO = S.SEGUNDO_APELLIDO_S,
    U.OTROS_APELLIDOS = S.OTROS_APELLIDOS_S,
    U.PASSWORD = S.PASSWORD_S,
    U.CALLE = S.CALLE_S,
    U.COLONIA = S.COLONIA_S,
    U.ZONA = S.ZONA_S,
    U.CIUDAD = S.CIUDAD_S,
    U.MUNICIPIO = S.MUNICIPIO_S,
    U.DEPARTAMENTO = S.DEPARTAMENTO_S,
    U.CODIGO_POSTAL = S.CODIGO_POSTAL_S,
    U.TELEFONO = S.TELEFONO_S,
    U.NIT = S.NIT_S,
    U.DPI = S.DPI_S,
    U.ID_ROL = S.ID_ROL_S
WHEN NOT MATCHED THEN
INSERT (
        U.NOMBRE_USUARIO,
        U.PRIMER_NOMBRE,
        U.SEGUNDO_NOMBRE,
        U.PRIMER_APELLIDO,
        U.SEGUNDO_APELLIDO,
        U.OTROS_APELLIDOS,
        U.PASSWORD,
        U.CALLE,
        U.COLONIA,
        U.ZONA,
        U.CIUDAD,
        U.MUNICIPIO,
        U.DEPARTAMENTO,
        U.CODIGO_POSTAL,
        U.TELEFONO,
        U.NIT,
        U.DPI,
        U.ID_ROL
) VALUES (
        S.NOMBRE_USUARIO_S,
        S.PRIMER_NOMBRE_S,
        S.SEGUNDO_NOMBRE_S,
        S.PRIMER_APELLIDO_S,
        S.SEGUNDO_APELLIDO_S,
        S.OTROS_APELLIDOS_S,
        S.PASSWORD_S,
